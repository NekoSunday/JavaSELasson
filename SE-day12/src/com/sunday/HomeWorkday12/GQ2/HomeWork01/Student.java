package com.sunday.HomeWorkday12.GQ2.HomeWork01;

public class Student {
    private int score;
    private String name;

    public Student() {
    }

    public Student(int score, String name) {
        this.score = score;
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
